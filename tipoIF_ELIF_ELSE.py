# blocos de código + if / elif / else (condicionais)

entrada = input("Digite 'entrar' ou 'sair': ")

if entrada == "entrar":
    print("Você entrou no sistema.")
elif entrada == "sair":
    print("Você saiu do sistema.")
else:
    print("Opção inválida. Por favor, digite 'entrar' ou 'sair'.")
