# Concatenação (+) e repetição (*) com operadores aritméticos

concatenação = "Python" + " " + "é" + " " + "divertido!"
repetição = "Python " * 3
# Exibindo os resultados
print("Concatenação:", concatenação)
print("Repetição:", repetição)

# Concatenação e repetição com variáveis
nome = "Emerson"
sobrenome = "S Motta"
nome_completo = nome + " " + sobrenome
print("Nome completo:", nome_completo)

# Repetição de uma string
repetição_nome = nome * 2
print("Repetição do nome:", repetição_nome)